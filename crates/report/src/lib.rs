/// Generate GitHub Actions CI workflow template for continuous fuzzing.
pub fn generate_github_actions_template(program_name: &str, iterations: u64) -> String {
    format!(
        r#"name: Krastor Fuzz

on:
  push:
    branches: [main]
  pull_request:
    branches: [main]
  schedule:
    - cron: '0 2 * * *'  # Daily at 2 AM

jobs:
  fuzz:
    runs-on: ubuntu-latest
    steps:
      - uses: actions/checkout@v4
      - uses: actions-rust-lang/setup-rust-toolchain@v1
        with:
          toolchain: stable
      - name: Install Solana CLI
        run: |
          sh -c "$(curl -sSfL https://release.anza.xyz/stable/install)"
          echo "$HOME/.local/share/solana/install/active_release/bin" >> $GITHUB_PATH
      - name: Install Krastor
        run: cargo install cargo-krastor
      - name: Build program
        run: anchor build
      - name: Run fuzz
        run: krastor fuzz run --iterations {iterations} --output coverage.html
      - name: Upload coverage report
        uses: actions/upload-artifact@v4
        if: always()
        with:
          name: krastor-coverage
          path: coverage.html
      - name: Upload crash reports
        uses: actions/upload-artifact@v4
        if: failure()
        with:
          name: krastor-crashes
          path: crashes/
"#,
        iterations = iterations,
    )
}

/// Save a crash record to the crashes/ directory.
pub fn persist_crash(crash: &CrashRecord, output_dir: &Path) -> anyhow::Result<()> {
    std::fs::create_dir_all(output_dir)?;
    let filename = format!(
        "crash_{}_{}.json",
        crash.crash_type.to_string().to_lowercase().replace('(', "_").replace(')', ""),
        crash.discovered_at_round,
    );
    crash.save(&output_dir.join(filename))?;
    Ok(())
}

#[cfg(test)]
mod tests {
    use super::*;
    use krastor_fuzz_core::CoverageBitmap;
    use krastor_fuzz_core::crash::{CrashRecord, CrashType};
    use krastor_fuzz_core::FuzzActionSequence;

    #[test]
    fn test_generate_html_report() {
        let cov = CoverageBitmap { edges: vec![1u8; 1000], covered_edges: 500 };
        let crashes = vec![
            CrashRecord {
                original_sequence: FuzzActionSequence { actions: vec![], initial_accounts: vec![] },
                minimal_sequence: FuzzActionSequence { actions: vec![], initial_accounts: vec![] },
                description: "test crash".into(),
                crash_type: CrashType::ExecutionError,
                discovered_at_round: 42,
                timestamp: "2026-01-01T00:00:00Z".into(),
                instructions_removed: 5,
            }
        ];

        let html = generate_html_report(&cov, &crashes, 10000, "test_prog");
        assert!(html.contains("test_prog"));
        assert!(html.contains("coverage"));
        assert!(html.contains("ExecutionError"));
        assert!(html.contains("Krastor"));
    }

    #[test]
    fn test_github_actions_template() {
        let tmpl = generate_github_actions_template("my_prog", 50000);
        assert!(tmpl.contains("krastor fuzz"));
        assert!(tmpl.contains("my_prog"));
        assert!(tmpl.contains("50000"));
        assert!(tmpl.contains("upload-artifact"));
    }
}